    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        
        String uri[] = this.jTextFieldHostInfo.getText().split(":");
        
       
        if((!this.jTextFieldHostInfo.getText().isEmpty())&&(!uri[0].isEmpty())&&(!uri[1].isEmpty()))
        {
           
            this.configuration.setConfigVariable("IP",uri[0]);
            this.configuration.setConfigVariable("PORT",uri[1]);
            
            this.jLabelMessage.setText("");
            
            this.dispose();
        }
        else
        {
            this.jLabelMessage.setText("Please fill empty field !");
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

